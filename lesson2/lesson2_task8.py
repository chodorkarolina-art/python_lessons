# Rzutowanie typów: Utwórz zmienną liczba_str = "5.8" . 
# Przekonwertuj ją najpierw na float , a następnie na int i wyświetl wyniki obu konwersji. 
# Co zauważyłeś podczas konwersji float na int ?

liczba_str = "5.8"

liczba_float = (float(liczba_str))
print("Float:", liczba_float)

liczba_int = int(liczba_float)
print("Int:", liczba_int)

